def test_manifest_discovery_excludes_untracked_and_root_workspace_member_locks(
    tmp_path: Path,
) -> None:
    repo = _write_fixture(tmp_path)
    root_manifest = repo / "Cargo.toml"
    root_manifest.write_text(
        root_manifest.read_text().replace('members = ["."]', 'members = [".", "member"]')
    )
    member = repo / "member"
    member.mkdir()
    member_manifest = member / "Cargo.toml"
    member_manifest.write_text('[package]\nname = "member"\nversion = "0.1.0"\n')
    member_lock = member / "Cargo.lock"
    member_lock.write_text(
        'version = 4\n\n[[package]]\nname = "fortress-rollback"\nversion = "0.1.0"\n'
    )
    _track(repo, root_manifest, member_manifest, member_lock)
    _write_standalone(
        repo,
        "target/generated",
        'version = 4\n\n[[package]]\nname = "fortress-rollback"\nversion = "0.1.0"\n',
        track=False,
    )

    result = _list_manifests(repo)

    assert result.returncode == 0, result.stdout + result.stderr
    assert result.stdout == ""


